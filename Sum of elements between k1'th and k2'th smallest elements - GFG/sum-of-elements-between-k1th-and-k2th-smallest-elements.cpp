class Solution{
    public:
    long long sumBetweenTwoKth( long long a[], long long N, long long x, long long y)
    {
        long ans=0;
        sort(a, a+N);
        for(int i=x;i<y-1;i++){
            ans+=a[i];
        }
        return ans;
    }
};

